type TranslationKey = keyof typeof STRINGS.es;

export function setUiLanguage(language?: string): UiLanguage {
  currentLanguage = normalizeLanguage(language);
  return currentLanguage;
}

export function getUiLanguage(): UiLanguage {
  return currentLanguage;
}

export function getUiLocale(): string {
  return currentLanguage === 'en' ? 'en-US' : 'es-ES';
}

export function normalizeLanguage(language?: string): UiLanguage {
  if (!language) return 'es';
  const normalized = language.trim().toLowerCase();
  return normalized === 'en' || normalized === 'en-us' || normalized === 'english'
    ? 'en'
    : 'es';
}

export function t(key: TranslationKey): string {
  return STRINGS[currentLanguage][key];
}

export function formatDateShort(iso: string): string {
  try {
    return new Date(iso).toLocaleDateString(getUiLocale(), { month: 'short', day: 'numeric' });
  } catch {
    return '';
  }
}

export function formatDateTime(iso?: string): string {
  if (!iso) return '';
  try {
    return new Date(iso).toLocaleDateString(getUiLocale(), {
      year: 'numeric',
      month: 'short',
      day: 'numeric',
    });
  } catch {
    return '';
  }
}

export function formatTime(iso: string): string {
  try {
    return new Date(iso).toLocaleTimeString(getUiLocale(), { hour: '2-digit', minute: '2-digit' });
  } catch {
    return '';
  }
}

